package Day3_05_March_1;
public class DataTypes {
    public static void main(String[] args){
        byte a = 'a';
        System.out.println("This is byte: "+a);

        long l = 1234567890987l;
        System.out.print("This is long: "+l);
        long l2 = 12;
        System.out.println("This is long: "+l2);
        System.out.println("This is long type: "+typeOf(l2));
    }
    
}
